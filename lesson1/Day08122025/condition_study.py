#In trẻ con (0-11) / Thiếu niên (11-17) / Thanh niên(18-30) / Người già (trên 30)

age = int(input("Nhập tuổi vào đây: "))

if age>11:
    print("Thiếu niên")
elif age>17:
    print("Thanh niên")
elif age>30:
    print("Người già")
else:
    print("trẻ con")

